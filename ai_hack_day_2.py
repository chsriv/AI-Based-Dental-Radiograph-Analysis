def map_to_fdi(centroid, shape):
    cX, cY = centroid
    W, H = shape[1], shape[0]

    # Divide the image into the 4 dental quadrants
    # Q1: Upper Right, Q2: Upper Left, Q3: Lower Left, Q4: Lower Right
